#Получаем списки чисел
s1 = set(map(int, input("Введите числа первого массива через пробел:\n").split()))
s2 = set(map(int, input("Введите числа второго массива через пробел:\n").split()))

#Считаем, сколько чисел содержится одновременно как в первом списке, так и во втором и выводим результат
print("Всего пересекающихся чисел:", len(s1.intersection(s2)))